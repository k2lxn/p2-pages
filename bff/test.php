           		  <h2 id="about">About - What is Bee Friendly Farming?</h2>

<p>Bee Friendly Farming (BFF) is a program that provides guidelines for farmers and growers interested in promoting pollinator health on their lands​.​
  <br>
  <a href="PDFs/Bee-Friendlyarticle_AcresUSA.pdf" target="_blank">Read a recent article on a featured Bee Friendly Farmer. </a><br>
  <br>
<h4 id="criteria">What is the Criteria to be Bee Friendly Farming certified?</h4>
 <p>1.   Offer forage providing good nutrition for bees on 3-6% of land.
<br>
2.   Plant continuous bloom of​ different flowering plants throughout the growing season, especially in ​early ​spring and ​late autumn​ in temperate regions.
<br>
3.   Offer clean water for bees.
<br>
4.   Provide a variety of habitat​ for nesting and mating,​ through features such as hedgerows, natural brush, or bufferstrips.
<br>
5.   Practice Integrated Pest Management (IPM); reduce or eliminate the use of chemicals.
<br>
6.   Pay the annual $35 certification fee.<br>
<br/>
Once qualified, your location is pinned on a map and you are then able to use the BFF logo to indicate that your products or services are produced on lands that promote pollinator health. Signs may be purchased for your farm. Additionally, parts of the affordable annual fees collected ​are returned to members in the form of mini-grants for pollinator plantings.

<h4>What can you do?</h4>
                                        <p>1.<a href="http://pollinator.org/guides.htm"> Plant for pollinators - Get a free planting guide specific to your ecoregion</a><br>
                                          2.<a href="#certify"> Certify your farm </a><br>
                                          3. <a href="mailto:beefriendlyfarmer@gmail.com">Share your story with us </a><br>
                                          4. <a href="http://pollinator.org/donate.htm">Donate to support and expand the program </a></p>
<h4>History<img src="newweb/newweb/P2/images/SHARElogoTextFINAL.png" align="right"  width="360" height="196"></h4>
                    					<p>​Bee Friendly Farming is an online, self-certification program​ created by Kathy Kellison and other experts (Sam Droege, Mace Vaughn, Dennis vanEnglesdorp, Marla Spivak, Randy Oliver, Robbin Thorp, Karen Strickler, Gerry Miller, Jeff Anderson, Phil Giles, and Gene Brandi​).Bee Friendly Farming was also known as Partners for Sustainable Pollination (PFSP) and has now merged with Pollinator Partnership.  The two organizations entered into a Memorandum of Understanding in April 2013 to create the BFF-S.H.A.R.E. (Simply Have Areas Reserved for the Environment) partnership.   This was a great step forward for the BFF program as those who enroll in the S.H.A.R.E. program will also now be made aware of the option of further recognition through certification as a Bee Friendly Farm or Garden.<br/>
                    					</p>

