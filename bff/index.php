               		  <h2 class="title"><span id="bff">Bee Friendly Farming</span></h2>
               		  <p dir="ltr" id="docs-internal-guid-862c2824-c08c-a24a-6a4a-76f0cde9f509">Pollinators are an indispensable natural resource in agriculture and healthy ecosystems. Farmers and beekeepers are on the front line in playing a critical role for their survival.<br>
                   		  <br>
                          <a href="#map_view" target="_blank"><img src="newweb/P2/images/bffsign3.png" border="0"></a><br>
               		  </p>
                          <div class="col-lg-12" id="about">
                              <h2>About - What is Bee Friendly Farming?</h2>

                              <p>Bee Friendly Farming (BFF) is a program that provides guidelines for farmers and growers interested in promoting pollinator health on their lands​.​
                                  <br>
                                  <a href="/../PDFs/Bee-Friendlyarticle_AcresUSA.pdf" target="_blank">Read a recent article on a featured Bee Friendly Farmer. </a><br>
                                  <br>
                                  <!--START CRITERIA LIST-->
                              <div class="col-md-12">
                                  <h4 id="criteria">What is the Criteria to be Bee Friendly Farming certified?</h4>
                                  <ol>
                                      <li>Offer forage providing good nutrition for bees on 3-6% of land.</li>
                                      <li>Plant continuous bloom of​ different flowering plants throughout the growing season, especially in ​early ​spring and ​late autumn​ in temperate regions.</li>
                                      <li>Offer clean water for bees.</li>
                                      <li>Provide a variety of habitat​ for nesting and mating,​ through features such as hedgerows, natural brush, or bufferstrips.</li>
                                      <li>Practice Integrated Pest Management (IPM); reduce or eliminate the use of chemicals.</li>
                                      <li> Pay the annual $35 certification fee.</li>
                                  </ol>
                              </div>
