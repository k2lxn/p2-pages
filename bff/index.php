                              <!--START HISTORY-->
                              <div class="col-md-12">
                                  <h4>History<img src="newweb/P2/images/SHARElogoTextFINAL.png" align="right"  width="360" height="196"></h4>
                                  <p>​Bee Friendly Farming is an online, self-certification program​ created by Kathy Kellison and other experts (Sam Droege, Mace Vaughn, Dennis vanEnglesdorp, Marla Spivak, Randy Oliver, Robbin Thorp, Karen Strickler, Gerry Miller, Jeff Anderson, Phil Giles, and Gene Brandi​).Bee Friendly Farming was also known as Partners for Sustainable Pollination (PFSP) and has now merged with Pollinator Partnership.  The two organizations entered into a Memorandum of Understanding in April 2013 to create the BFF-S.H.A.R.E. (Simply Have Areas Reserved for the Environment) partnership.   This was a great step forward for the BFF program as those who enroll in the S.H.A.R.E. program will also now be made aware of the option of further recognition through certification as a Bee Friendly Farm or Garden.<br/></p>
